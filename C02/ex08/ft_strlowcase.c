char	*ft_strlowcase(char *str)
{
	int	i;

	if (*str == '\0')
	{
		return (str);
	}
	i = 0;
	while (*str)
	{
		if (*str >= 65 && *str <= 90)
		{
			*str += 32;
		}
		str++;
		i++;
	}
	str = str - i;
	return (str);
}
